        cubeShader.setMat4f("model", model);
        cubeShader.setMat4f("view", view);
        cubeShader.setMat4f("projection", projection);


        glBindVertexArray(VAO1);
        glDrawArrays(GL_TRIANGLES, 0, 36);

        lampShader.use();
        
        model = glm::mat4(1.0f);
